CREATE TABLE IF NOT EXISTS users (
    id BIGINT NOT NULL AUTO_INCREMENT,
    username VARCHAR(50) NOT NULL,
    full_name VARCHAR(100) NOT NULL,
    email VARCHAR(100) NOT NULL,
    password VARCHAR(255) NOT NULL,
    phone_number VARCHAR(20),
    role VARCHAR(20) NOT NULL,
    account_type VARCHAR(20),
    date_of_birth DATE,
    gender VARCHAR(20),
    address VARCHAR(255),
    profile_image_path VARCHAR(255),
    license_number VARCHAR(50),
    license_expiry_date DATE,
    license_document_path VARCHAR(255),
    id_document_path VARCHAR(255),
    vehicle_plate VARCHAR(20),
    vehicle_model VARCHAR(100),
    vehicle_year INT,
    vehicle_color VARCHAR(30),
    company_registration_path VARCHAR(255),
    company_registration_no VARCHAR(50),
    company_name VARCHAR(100),
    company_email VARCHAR(100),
    company_phone VARCHAR(20),
    company_location VARCHAR(255),
    company_description VARCHAR(1000),
    email_verified BIT(1) DEFAULT b'1',
    enabled BIT(1) DEFAULT b'1',
    account_locked BIT(1) DEFAULT b'0',
    created_at DATETIME NOT NULL,
    updated_at DATETIME NULL,
    last_login_at DATETIME NULL,
    password_changed_at DATETIME NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uk_users_username (username),
    UNIQUE KEY uk_users_email (email),
    KEY idx_user_role (role),
    KEY idx_user_account_type (account_type),
    KEY idx_user_enabled (enabled)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS companies (
    id BIGINT NOT NULL AUTO_INCREMENT,
    user_id BIGINT NULL,
    company_name VARCHAR(150) NOT NULL,
    company_email VARCHAR(100) NULL,
    company_phone VARCHAR(20) NULL,
    registration_number VARCHAR(50) NULL,
    tax_id VARCHAR(50) NULL,
    address VARCHAR(255) NULL,
    city VARCHAR(100) NULL,
    region VARCHAR(100) NULL,
    postal_code VARCHAR(20) NULL,
    description VARCHAR(1000) NULL,
    logo_path VARCHAR(255) NULL,
    website VARCHAR(255) NULL,
    status VARCHAR(20) NOT NULL,
    verified_at DATETIME NULL,
    verification_notes VARCHAR(500) NULL,
    wallet_balance DECIMAL(15,2) NOT NULL DEFAULT 0.00,
    commission_rate DECIMAL(5,2) NOT NULL DEFAULT 10.00,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uk_companies_user (user_id),
    UNIQUE KEY uk_companies_name (company_name),
    UNIQUE KEY uk_companies_email (company_email),
    UNIQUE KEY uk_companies_registration (registration_number),
    KEY idx_company_status (status),
    CONSTRAINT fk_companies_user FOREIGN KEY (user_id) REFERENCES users (id)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS cars (
    id BIGINT NOT NULL AUTO_INCREMENT,
    driver_id BIGINT NULL,
    company_id BIGINT NULL,
    plate_number VARCHAR(20) NOT NULL,
    car_brand VARCHAR(50) NOT NULL,
    model VARCHAR(50) NOT NULL,
    `year` INT NULL,
    color VARCHAR(30) NULL,
    total_seats INT NOT NULL DEFAULT 18,
    vin VARCHAR(50) NULL,
    chassis_number VARCHAR(50) NULL,
    engine_number VARCHAR(50) NULL,
    status VARCHAR(20) NOT NULL,
    roadworthy_expiry DATETIME NULL,
    insurance_expiry DATETIME NULL,
    last_inspection_date DATETIME NULL,
    image_path VARCHAR(255) NULL,
    description VARCHAR(500) NULL,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uk_cars_plate (plate_number),
    KEY idx_car_driver (driver_id),
    KEY idx_car_company (company_id),
    KEY idx_car_status (status),
    CONSTRAINT fk_cars_driver FOREIGN KEY (driver_id) REFERENCES users (id),
    CONSTRAINT fk_cars_company FOREIGN KEY (company_id) REFERENCES companies (id)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS trips (
    id BIGINT NOT NULL AUTO_INCREMENT,
    car_id BIGINT NOT NULL,
    driver_id BIGINT NULL,
    company_id BIGINT NULL,
    from_location VARCHAR(100) NOT NULL,
    to_location VARCHAR(100) NOT NULL,
    pickup_station VARCHAR(150) NULL,
    drop_off_station VARCHAR(150) NULL,
    departure_time DATETIME NOT NULL,
    arrival_time DATETIME NULL,
    trip_amount DECIMAL(10,2) NOT NULL,
    available_seats INT NOT NULL,
    total_seats INT NOT NULL,
    description VARCHAR(500) NULL,
    status VARCHAR(50) NOT NULL,
    cancel_reason VARCHAR(100) NULL,
    cancel_reason_details VARCHAR(500) NULL,
    cancelled_by BIGINT NULL,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NULL,
    expired_at DATETIME NULL,
    PRIMARY KEY (id),
    KEY idx_trip_status (status),
    KEY idx_trip_departure (departure_time),
    KEY idx_trip_driver (driver_id),
    KEY idx_trip_company (company_id),
    KEY idx_trip_car (car_id),
    KEY idx_trip_from_to (from_location, to_location),
    CONSTRAINT fk_trips_car FOREIGN KEY (car_id) REFERENCES cars (id),
    CONSTRAINT fk_trips_driver FOREIGN KEY (driver_id) REFERENCES users (id),
    CONSTRAINT fk_trips_company FOREIGN KEY (company_id) REFERENCES companies (id),
    CONSTRAINT fk_trips_cancelled_by FOREIGN KEY (cancelled_by) REFERENCES users (id)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS wallets (
    id BIGINT NOT NULL AUTO_INCREMENT,
    user_id BIGINT NOT NULL,
    balance DECIMAL(15,2) NOT NULL DEFAULT 0.00,
    loyalty_points DECIMAL(15,2) NOT NULL DEFAULT 0.00,
    currency VARCHAR(3) NOT NULL DEFAULT 'GHS',
    is_active BIT(1) DEFAULT b'1',
    last_transaction_at DATETIME NULL,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uk_wallets_user (user_id),
    KEY idx_wallet_currency (currency),
    CONSTRAINT fk_wallets_user FOREIGN KEY (user_id) REFERENCES users (id)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS seat_maps (
    id BIGINT NOT NULL AUTO_INCREMENT,
    trip_id BIGINT NOT NULL,
    car_id BIGINT NOT NULL,
    seat_number VARCHAR(10) NOT NULL,
    `row_number` INT NULL,
    column_label VARCHAR(5) NULL,
    seat_type VARCHAR(20) NOT NULL,
    extra_legroom BIT(1) DEFAULT b'0',
    status VARCHAR(20) NOT NULL,
    held_by_booking_id BIGINT NULL,
    hold_expires_at DATETIME NULL,
    created_at DATETIME NOT NULL,
    updated_at DATETIME NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uk_seat_maps_trip_seat (trip_id, seat_number),
    KEY idx_seatmap_status (status),
    KEY idx_seatmap_car (car_id),
    CONSTRAINT fk_seat_maps_trip FOREIGN KEY (trip_id) REFERENCES trips (id),
    CONSTRAINT fk_seat_maps_car FOREIGN KEY (car_id) REFERENCES cars (id)
) ENGINE=InnoDB;

